package day07_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int num1= 10;
        int num2= 3;
        int div= num1/num2;
        int mod=num1%num2;
        System.out.println(num1+ " divide by " + num2 +" is equal to "+div+" with a remainder of "+mod+".");
    }
}
